package fcu.iecs.oop.pokemon;

public class Pokemon {
	private final String name = null;
	
	private int cp;

	public Pokemon(String name , int cp)
	{
		name = this.name;
		cp = this.cp;
	}
	
	public int getCp() {
		return cp;
	}
	public void setCp(int cp) {
		this.cp = cp;
	}
	public String getName() {
		return name;
	}
	

}
